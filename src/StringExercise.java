public class StringExercise {
    public static void main(String[] args) {
        String output = "We don't need no education";
        System.out.println(output + "\n" + output.replace("education","thought control"));
        output = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(output);
        output = "In windows, the main drive is usually C:\\";
        System.out.println(output);
        output = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(output);
    }
}
